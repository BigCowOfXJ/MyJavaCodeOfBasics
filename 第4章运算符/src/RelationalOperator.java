public class RelationalOperator {
    public static void main(String[] args){
        /*
         * 关系运算符的结果大多都是 false/true 即boolean类型
         * 有== != < > <= >= instanceof(检查是否是类的对象)
         * 常见的错误 会把 == 误写成 =
         * 
         * 代码说明
         * 举例 ，没啥好说明的，复制看看结果就行
         */

        int a = 9;
        int b = 8;
        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(a == b);
        System.out.println(a <= b);
        System.out.println(a >= b);



    }
}
